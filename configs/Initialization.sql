CREATE TABLE HISTRECORDS(
    DAY INTEGER,
    MONTH INTEGER,
    YEAR INTEGER,
    HOUR INTEGER,
    MINUTE INTEGER,
    TEMP REAL,
    HUM REAL
);

CREATE TABLE EVENTS(
    START_TEMP REAL,
    PERIODIC TEXT,
    BY_TIME INTEGER,
    START_HOUR INTEGER,
    START_MIN INTEGER,
    END_HOUR INTEGER,
    END_MIN INTEGER,
    ACTIVE INTEGER
);
